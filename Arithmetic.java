package Operators;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 12;
		int b = 34;
		int c = a + b;
		System.out.println("\n Addition is = " + c);

		c = a - b;
		System.out.println("\n Subtraction is = " + c);

		c = a * b;
		System.out.println("\n Multiplication is = " + c);

		c = a / b;
		System.out.println("\n Division is = " + c);

		c = a % b;
		System.out.println("\n Modulous is = " + c);
	}
}
